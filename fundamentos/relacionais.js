// == (igual)
console.log('1' == 1); //ele ignora o tipo e só diz se o valor é igual, logo vai retornar "true"

// === (estritamente igual)
console.log('1' === 1); //ele vê se o tipo e o valor são iguais.. como não são, vai retornar "false";

// != (diferente)
console.log('1' != 1); //vai retornar false, porque os valores são iguais

// !== (estritamente diferente)
console.log('1' !== 1); //vai retornar true, porque só os valores são iguais.. os tipos são diferentes


